

// function to calculate sum, difference, multiplication, and division of two numbers.
function calculate(a,b){
    var addition = a+b;
    var subtraction = a-b;
    var multiplication = a*b;
    var division = a/b;

    return {
        addition: addition,
        subtraction: subtraction,
        multiplication: multiplication,
        division: division
    }
    
}
